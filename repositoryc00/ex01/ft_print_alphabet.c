#include <unistd.h>

void	ft_print_alphabet(void)
{
	char	a;

	a = 'a';
	while(a < 123)
	{
		write(1, &a, 1);
		a++;
	}
}
int main()
{
	ft_print_alphabet();
		return 0;
}
	
